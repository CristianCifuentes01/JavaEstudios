package mypeopleexample;

public class Person {
	 private String nombre;
	    private String address;

	    public String getName() {
	        return nombre;
	    }

	    public void setName(String name) {
	        this.nombre = name;
	    }

	    public String getAddress() {
	        return address;
	    }

	    public void setAddress(String address) {
	        this.address = address;
	    }
}
